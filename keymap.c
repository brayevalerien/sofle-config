// RGB: purple reactive fade — lights up per key on press, fades to black
void keyboard_post_init_user(void) {
    rgb_matrix_enable_noeeprom();
    rgb_matrix_mode_noeeprom(RGB_MATRIX_SOLID_REACTIVE_WIDE);
    rgb_matrix_sethsv_noeeprom(191, 255, 200); // purple
}

// Encoders: left = volume, right = page scroll
bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        tap_code(clockwise ? KC_VOLU : KC_VOLD);
    } else {
        tap_code(clockwise ? MS_WHLD : MS_WHLU);
    }
    return false;
}

