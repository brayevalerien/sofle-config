void render_cat(void) {
    if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
        anim_frame++;
        anim_timer = timer_read32();
    }

    uint8_t wpm = get_current_wpm();
    oled_set_cursor(0, 0);

    if (wpm == 0) {
        oled_write_raw_P(neko_sleep, SPRITE_SIZE);
    } else if (wpm < 20) {
        oled_write_raw_P(neko_awake, SPRITE_SIZE);
    } else if (wpm < 60) {
        oled_write_raw_P(neko_walk[anim_frame % 2], SPRITE_SIZE);
    } else {
        oled_write_raw_P(neko_run[anim_frame % 2], SPRITE_SIZE);
    }
}

// Left OLED: layout (static ERGOL) + layer + caps + WPM
void render_status(void) {
    oled_set_cursor(0, 0);

    oled_write_P(PSTR("LAYT "), false);
    oled_write_P(PSTR("ERGL "), false);
    oled_write_P(PSTR("     "), false);

    oled_write_P(PSTR("LAYR "), false);
    switch (get_highest_layer(layer_state)) {
        case _BASE: oled_write_P(PSTR("BASE "), false); break;
        case _NAV:  oled_write_P(PSTR("NAV  "), false); break;
        case _FN:   oled_write_P(PSTR("FN   "), false); break;
        default:    oled_write_P(PSTR("???  "), false); break;
    }
    oled_write_P(PSTR("     "), false);

    led_t led_state = host_keyboard_led_state();
    oled_write_P(PSTR("CAPS "), false);
    oled_write_P(led_state.caps_lock ? PSTR("ON   ") : PSTR("off  "), false);
    oled_write_P(PSTR("     "), false);

    oled_write_P(PSTR("WPM  "), false);
    oled_write(get_u8_str(get_current_wpm(), ' '), false);
}

bool oled_task_user(void) {
    if (is_keyboard_master()) {
        render_status();
    } else {
        render_cat();
    }
    return false;
}

